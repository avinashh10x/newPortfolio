const HEX_CLIP =
  "polygon(50% 0%, 100% 25%, 100% 75%, 50% 100%, 0% 75%, 0% 25%)";

/* ─── single hexagonal cell with mouse-follow glow ─── */
function TechHex({ item }: { item: TechItem }) {
  const cardRef = useRef<HTMLDivElement>(null);
  const glowRef = useRef<HTMLDivElement>(null);

  const handleMove = useCallback(
    (e: React.MouseEvent<HTMLDivElement>) => {
      const card = cardRef.current;
      const glow = glowRef.current;
      if (!card || !glow) return;
      const rect = card.getBoundingClientRect();
      const x = e.clientX - rect.left;
      const y = e.clientY - rect.top;
      glow.style.background = `radial-gradient(100px circle at ${x}px ${y}px, ${item.color}22, transparent 70%)`;
      const cx = rect.width / 2;
      const cy = rect.height / 2;
      card.style.transform = `perspective(600px) rotateX(${((y - cy) / cy) * -5}deg) rotateY(${((x - cx) / cx) * 5}deg) scale(1.08)`;
    },
    [item.color],
  );

  const handleLeave = useCallback(() => {
    if (cardRef.current)
      cardRef.current.style.transform =
        "perspective(600px) rotateX(0) rotateY(0) scale(1)";
    if (glowRef.current) glowRef.current.style.background = "transparent";
  }, []);

  return (
    <div
      ref={cardRef}
      onMouseMove={handleMove}
      onMouseLeave={handleLeave}
      data-anim="tech"
      className="group relative flex cursor-default flex-col items-center justify-center gap-1.5 overflow-hidden bg-foreground/3 transition-all duration-300 hover:bg-foreground/6"
      style={{
        clipPath: HEX_CLIP,
        willChange: "transform",
        aspectRatio: "0.866",
        width: "100%",
      }}
    >
      <div
        ref={glowRef}
        className="pointer-events-none absolute inset-0 z-0 transition-[background] duration-200"
      />
      <svg
        viewBox="0 0 24 24"
        className="relative z-10 w-7 h-7 md:w-8 md:h-8 transition-transform duration-300 group-hover:scale-125"
        fill={item.color}
        aria-label={item.name}
      >
        <title>{item.name}</title>
        <path d={item.icon} />
      </svg>
    </div>
  );
}

/* ─── responsive column count ─── */
function useHexCols() {
  const [cols, setCols] = useState(5);
  useEffect(() => {
    const update = () => {
      const w = window.innerWidth;
      setCols(w < 480 ? 3 : w < 768 ? 4 : 5);
    };
    update();
    window.addEventListener("resize", update);
    return () => window.removeEventListener("resize", update);
  }, []);
  return cols;
}

